package mvc;

/**
 * Student data model represents an object or JAVA POJO carrying data. It can also have logic to update controller if
 * its data changes.
 */
public class Student {
    private String rollNo;
    private String name;

    public String getRollNo() {
        return rollNo;
    }

    public void setRollNo(String rollNo) {
        this.rollNo = rollNo;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
